<li>无法被一些搜索引擎索引到</li>
<li>多数小型的移动设备（<em>PDA</em> 手机）无法完全显示框架</li>
<li>由于上面诸多缺点，因此不符合标准网页设计的理念，已经被标准网页设计抛弃</li>
</ul>
<p>目前框架的所有优点完全可以使用 <em>Ajax</em> 实现，因此已经没有必要使用 <em>iframe</em> 框架了。</p>
</blockquote>
