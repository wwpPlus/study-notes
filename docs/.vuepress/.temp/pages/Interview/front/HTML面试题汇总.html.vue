<h2 id="_28-iframe-的作用" tabindex="-1"><a class="header-anchor" href="#_28-iframe-的作用" aria-hidden="true">#</a> 28. <em>iframe</em> 的作用</h2>
<blockquote>
<p>参考答案：</p>
<p>参考答案：</p>
<p><em>iframe</em> 也称作嵌入式框架，嵌入式框架和框架网页类似，它可以把一个网页的框架和内容嵌入在现有的网页中。</p>
<p><strong>优点</strong></p>
<ul>
<li>重载页面时不需要重载整个页面，只需要重载页面中的一个框架页(减少了数据的传输，增加了网页下载速度)</li>
