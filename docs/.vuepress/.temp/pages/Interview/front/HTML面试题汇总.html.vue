<h2 id="_16-前端页面有哪三层构成-分别是什么-作用是什么" tabindex="-1"><a class="header-anchor" href="#_16-前端页面有哪三层构成-分别是什么-作用是什么" aria-hidden="true">#</a> 16. 前端页面有哪三层构成，分别是什么？作用是什么？</h2>
<blockquote>
<p>参考答案：</p>
<p>分成：结构层、表示层、行为层。</p>
<ol>
<li>结构层（<em>structural layer</em>）</li>
</ol>
<p>由 <em>HTML</em> 或 <em>XHTML</em> 之类的标记语言负责创建。标签，也就是那些出现在尖括号里的单词，对网页内容的语义含义做出了描述，但这些标签不包含任何关于如何显示有关内容的信息。例如，<em>p</em> 标签表达了这样一种语义：“这是一个文本段。”</p>
<ol start="2">
<li>表示层（<em>presentation layer</em>）</li>
</ol>
<p>由 <em>CSS</em> 负责创建。 <em>CSS</em> 对“如何显示有关内容”的问题做出了回答。</p>
<ol start="3">
<li>行为层（<em>behaviorlayer</em>）</li>
</ol>
<p>负责回答“内容应该如何对事件做出反应”这一问题。这是 <em>Javascript</em> 语言和 <em>DOM</em> 主宰的领域。</p>
</blockquote>
<h2 id="_17-页面可见性-page-visibility-api-可以有哪些用途" tabindex="-1"><a class="header-anchor" href="#_17-页面可见性-page-visibility-api-可以有哪些用途" aria-hidden="true">#</a> 17. 页面可见性（<em>Page Visibility</em>）<em>API</em> 可以有哪些用途？</h2>
<blockquote>
<p>参考答案：</p>
<p>所谓页面可见性，就是获取当前页面的可见状态。因为对于用户来讲可以打开好多标签页面来回切换，然而始终只有一个页面处于显示状态。所以我们可以通过页面可见性（<em>Page Visibility</em>）<em>API</em> 来判断当前页面是显示状态还是隐藏状态。</p>
<p>常用的 <em>API</em> 有三个，<em>document.hidden</em> 返回一个布尔值，如果是 <em>true</em>，表示页面可见，<em>false</em> 则表示页面隐藏。不同页面之间来回切换，会触发 <em>visibilitychange</em> 事件，还有一个 <em>document.visibilityState</em>，表示页面所处的状态。</p>
</blockquote>
<h2 id="_18-quirks-怪癖-模式是什么-它和-standards-标准-模式有什么区别" tabindex="-1"><a class="header-anchor" href="#_18-quirks-怪癖-模式是什么-它和-standards-标准-模式有什么区别" aria-hidden="true">#</a> 18. <em>Quirks</em>（怪癖）模式是什么？它和 <em>Standards</em>（标准）模式有什么区别</h2>
<blockquote>
<p>参考答案：</p>
<p>以 <em>IE6</em> 为例，如果写了 <em>DTD</em>，就意味着这个页面将采用对 <em>CSS</em> 支持更好的布局，而如果没有，则采用兼容之前的布局方式。这就是 <em>Quirks</em> 模式（怪癖模式，诡异模式，怪异模式）。</p>
<p>区别：总体会有布局、样式解析和脚本执行三个方面的区别，这里列举一些比较常见的区别：</p>
<ul>
<li>
<p>盒模型：在 <em>W3C</em> 标准中，如果设置一个元素的宽度和高度，指的是元素内容的宽度和高度，而在 <em>Quirks</em> 模式下，<em>IE</em> 的宽度和高度还包含了 <em>padding</em> 和 <em>border</em>。</p>
</li>
<li>
<p>设置行内元素的高宽：在 <em>Standards</em> 模式下，给 &lt;span&gt; 等行内元素设置 <em>wdith</em> 和 <em>height</em> 都不会生效，而在 <em>Quirks</em> 模式下，则会生效。</p>
</li>
<li>
<p>设置百分比的高度：在 <em>Standards</em> 模式下，一个元素的高度是由其包含的内容来决定的，如果父元素没有设置百分比的高度，子元素设置一个百分比的高度是无效的</p>
</li>
<li>
<p>用 <em>margin:0 auto</em> 设置水平居中：使用 <em>margin:0 auto</em> 在 <em>Standards</em> 模式下可以使元素水平居中，但在 <em>Quirks</em> 模式下却会失效。</p>
</li>
</ul>
</blockquote>
