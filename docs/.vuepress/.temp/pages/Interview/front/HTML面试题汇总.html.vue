<h2 id="_7-iframe-框架有那些优缺点" tabindex="-1"><a class="header-anchor" href="#_7-iframe-框架有那些优缺点" aria-hidden="true">#</a> 7. <em>iframe</em> 框架有那些优缺点？</h2>
<blockquote>
<p>参考答案：</p>
<ul>
<li><em>iframe</em> 会阻塞主页面的 <em>Onload</em> 事件；</li>
<li><em>iframe</em> 和主页面共享连接池，而浏览器对相同域的连接有限制，所以会影响页面的并行加载。</li>
